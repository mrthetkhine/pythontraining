a = 2
b = 3
print("2 in binary ",bin(a))
print("3 in binary ",bin(b))

print("2 & 3 in binary ",bin(a&b), " decimal ", a & b)
print("2 | 3 in binary ",bin(a|b), " decimal ", a | b)
print("~ 2 in binary ",bin(~a), " decimal ", ~a)
print(" 2^3 in binary ",bin(a^3), " decimal ", (2^3))
print(" 2<<2 in binary ",bin(a<<2), " decimal ", (2<<2))
print(" 3>>1 in binary ",bin(b>>1), " decimal ", (3>>1))
print(" 4>>1 in binary ",bin(4>>1), " decimal ", (4>>1))